package org.oscarehr.integration.cdx.datatypes;

import oscar.util.StringUtils;

public class NameClass {
	String familyName;
	String givenName;
	String middleName; // Or Given Name 2 
	String prefix;
	String suffix;
	String fullName;

	public NameClass() {
		familyName = "";
		givenName = "";
		middleName = "";
		prefix = "";
		suffix = "";
		fullName = "";
	}

	public String getFamilyName() {
		return familyName;
	}

	public void setFamilyName(String familyName) {
		this.familyName = familyName;
	}

	public String getGivenName() {
		return givenName;
	}

	public void setGivenName(String givenName) {
		this.givenName = givenName;
	}

	public String getMiddleName() {
		return middleName;
	}

	public void setMiddleName(String middleName) {
		this.middleName = middleName;
	}

	public String getPrefix() {
		return prefix;
	}

	public void setPrefix(String prefix) {
		this.prefix = prefix;
	}

	public String getSuffix() {
		return suffix;
	}

	public void setSuffix(String suffix) {
		this.suffix = suffix;
	}

	public String getFullName() {
		return fullName;
	}

	public void setFullName(String fullName) {
		this.fullName = fullName;
	}

	public void BuildFullName() {
		String fullPersonName = familyName + ", ";
		if (!StringUtils.isNullOrEmpty(prefix)) {
			fullPersonName += prefix + " ";
		}
		if (!StringUtils.isNullOrEmpty(givenName)) {
			fullPersonName += givenName + " ";
		}
		if (!StringUtils.isNullOrEmpty(middleName)) {
			fullPersonName += middleName;
		}
		fullName = fullPersonName.trim();
	}
}
